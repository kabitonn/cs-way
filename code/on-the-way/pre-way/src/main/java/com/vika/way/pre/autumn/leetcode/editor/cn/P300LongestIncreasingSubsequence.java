    class Solution {
        public int lengthOfLIS1(int[] nums) {

            int n = nums.length;
            int[] dp = new int[n];
            int max = 0;
            for (int i = 0; i < n; i++) {
                dp[i] = 1;
                for (int j = 0; j < i; j++) {
                    if (nums[i] > nums[j]) {
                        dp[i] = Math.max(dp[i], dp[j] + 1);
                    }
                }
                max = Math.max(max, dp[i]);
            }
            return max;
        }

        public int lengthOfLIS(int[] nums) {
            int n = nums.length;
            int[] tail = new int[n];
            int len = 0;
            for (int i = 0; i < n; i++) {
                int left = 0;
                int right = len;
                while (left < right) {
                    int mid = (left + right) >>> 1;
                    if (tail[mid] < nums[i]) {
                        left = mid + 1;
                    } else {
                        right = mid;
                    }
                }
                tail[left] = nums[i];
                if (left == len) {
                    len++;
                }
            }
            return len;
        }
    }
